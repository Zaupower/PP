class Musica {
    protected int numeroDeFaixa;
    protected String nomeDeFaixa;
    protected float duracaoSecs;
    protected String autor;

    public Musica(int numeroDeFaixa, String nomeDeFaixa, float duracaoSecs, String autor) {
        this.numeroDeFaixa = numeroDeFaixa;
        this.nomeDeFaixa = nomeDeFaixa;
        this.duracaoSecs = duracaoSecs;
        this.autor = autor;
    }
    
    
}
